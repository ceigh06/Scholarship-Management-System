<h4>What is this scholarship?</h4>

<p>The Diwa ng Bayani Foundation Scholarship is a merit-based program that provides full tuition support to deserving students.</p>
<br><br>
<h4>How do I apply?</h4>

<p>Create an account using a valid email address, then log in to access the application form.</p>
<br><br>
<h4>Why do I need a valid email?</h4>

<p>Your email will be used to log in and receive updates about your application.</p>
<br><br>
<h4>How do I keep my account safe?</h4>

<p>Keep your User ID and personal information secure. Do not share your login details.</p>
<br><br>
<h4>Can I create more than one account?</h4>

<p>No. Multiple accounts are not allowed and may delay your application.</p>
<br><br>
<h4>What happens after I register?</h4>

<p>You can log in, fill out the form, and track your application.</p>
<br><br>
<h4>What if I can’t log in?</h4>

<p>Check your email and login details. Try again or contact support if needed.</p>
<br><br>
<h4>Is my information safe?</h4>

<p>Yes. Your data will be kept confidential and used only for the scholarship process.</p>
